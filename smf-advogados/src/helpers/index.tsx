export const getTranslation = (key: string, language: string): string => {
  const entryKey = translations.find((entry) => entry.key === key);

  if (entryKey) {
    const languageFound = entryKey.language.find(
      (entry) => entry.language === language
    );

    if (languageFound) {
      return languageFound.value;
    }
  }

  return "";
};
